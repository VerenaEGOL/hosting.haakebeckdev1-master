                  <h2 class="yellow">1918</h2>
                  <p class="center">Die Hemelinger Aktien Brauerei übernimmt am 1. Juli die Brauerei Haake & Co, und das gesamte Inlandsgeschäft der Kaiserbrauerei Beck & Co wird an die C.H. Haake Brauerei AG verkauft. </p>
