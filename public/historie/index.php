                  <p class="center">C.H. Haake errichtet den ersten Eiskeller am Neustadtsdeich, Ecke Sandweg, wo nach und nach das große Brauereizentrum Bremens entsteht. Über das Bremer Bier dieser Zeit wird Folgendes berichtet: „Meistens ist er (der Bremer) bei dem ursprünglichen Bremer Biere, welches aus Gerstenmalz und Hopfen gebraut wird, geblieben. Es gibt zwei Arten desselben, das sogenannte ‚Tönnchenbier', und das Bier vom Zapfen... Beides liefert ein gesundes, stärkendes Getränk." </p>
                
                  

