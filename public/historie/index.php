                  <p class="center">Johann Georg Graue, der Schwiegersohn von Conrad Hinrich Haake, übernimmt als erster kaufmännischer Betriebsleiter die Geschäftsführung und treibt gleichzeitig die Technisierung der Brauerei weiter voran.  </p>
                  <h2 class="yellow">1880</h2>
                  <p class="center">Richard Müller, der Schwiegersohn von Johann Georg Graue, führt das Geschäft nach Graues Tod auf eigene Rechnung weiter. Die C.H. Haake Brauerei ist unter den großen Bremer Brauereien der einzige Betrieb, der als Offene Handelsgesellschaft und damit ohne fremdes Kapital geführt wird.</p>
                  
